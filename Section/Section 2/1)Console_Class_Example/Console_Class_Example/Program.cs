class Sample
{
    static void Main()
    {

        // WriteLine prints the messages line by line
        System.Console.WriteLine("Welcome");
        System.Console.WriteLine("to");
        System.Console.WriteLine("C# Programming");

        System.Console.WriteLine("");

        // WriteLine prints the messages without new line 
        System.Console.Write("Welcome");
        System.Console.Write("to");
        System.Console.Write("C# Programming");

        // It waits for the user to press some key. Till then it doesn't close the window
        System.Console.ReadKey();


        // It clears the screen
        System.Console.Clear();

        System.Console.WriteLine("Thank You");
        System.Console.ReadKey();

    }
}